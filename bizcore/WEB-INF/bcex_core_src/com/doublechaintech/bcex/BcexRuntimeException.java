
package com.doublechaintech.bcex;


public class BcexRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public BcexRuntimeException() {
        super();
    }


    public BcexRuntimeException(String message) {
        super(message);
    }


    public BcexRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public BcexRuntimeException(Throwable cause) {
        super(cause);
    }


   
}









